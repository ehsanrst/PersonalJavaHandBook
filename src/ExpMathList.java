public class ExpMathList {
	public static void main(String[] args) {

		System.out.println(Math.pow(5, 3)); // power
		System.out.println(Math.abs(-4.3)); // absolute
		System.out.println(Math.ceil(7.3)); // round to upper value
		System.out.println(Math.floor(7.3));// round to lower value
		System.out.println(Math.max(8, 8.6));// maximum
		System.out.println(Math.min(7, 7.5));// minimum
		System.out.println(Math.sqrt(9)); // square root

	}
}
